    public boolean checkForm() {
        if (!data.checkIsEmpty(txtUsername)) {
            data.changColorLabel(false, lblerrUser, "User is not empty");
            return false;
        } else {
            data.changColorLabel(true, lblerrUser, null);
        }
        if (!data.checkIsEmpty(txtCurrentPasword)) {
            data.changColorLabel(false, lblErrcurentpass, "Current Password is not empty");
            return false;
        } else {
            data.changColorLabel(true, lblErrcurentpass, null);
        }
        if (!data.checkIsEmpty(txtNewpassword)) {
            data.changColorLabel(false, lblerrNewpass, "New password is not empty");
            return false;
        } else {
            data.changColorLabel(true, lblerrNewpass, null);
        }
        if (!data.checkIsEmpty(txtConfirmPass)) {
            data.changColorLabel(false, lblerrConfirmpass, "Confirm password is not empty");
            return false;
        } else {
            data.changColorLabel(true, lblerrConfirmpass, null);
        }
        return true;

    }

