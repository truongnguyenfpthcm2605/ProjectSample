    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (checkForm()) {
            String user = txtUsername.getText();
            String pass = txtCurrentPasword.getText();
            String newPass = new String(txtNewpassword.getPassword());
            String confirmPass = new String(txtConfirmPass.getPassword());
            if (!user.equals(Authentication.user.getMaNV())) {
                data.changColorLabel(false, lblerrUser, "User is not exist");
            }else if(!pass.equals(Authentication.user.getMatKhau())){
                data.changColorLabel(false, lblErrcurentpass, "Current Password is wrong");
            }else if(!newPass.equals(confirmPass)){
                    mess.messageNotify(this, "Please enter correct password ", "Error");
            }else{
                if(mess.confirmMessage(this,"Do you want to change password", "Change Password") == JOptionPane.YES_OPTION){
                    Authentication.user.setMatKhau(newPass);
                    nvDAO.update(Authentication.user);
                    mess.messageNotify(this, "Chang Password successfull", "Successful");
            }
            }

        }
    }//GEN-LAST:event_btnRegisterActionPerformed

