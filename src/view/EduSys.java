    private void openThongKe(int index) {
        if (Authentication.isLogin()) {
            if (index == 3 && !Authentication.isManager()) {
                mess.messageNotify(this, "You not role view turnover", "Role");
            } else {
                ThongKe tk = new ThongKe();
                tk.setVisible(true);
                tk.selectTab(index);
            }
        } else {
            mess.messageNotify(this, "Please Login", "Login");
        }
    }

