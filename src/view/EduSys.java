    @Override
    public void run() {
        while (true) {
            Date now = new Date();
            SimpleDateFormat formatDate = new SimpleDateFormat();
            formatDate.applyPattern("hh:mm:ss aa");
            String timeNow = formatDate.format(now);
            lblClock.setText(timeNow);
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                break;
            }
        }
    }

    private void initFrame() {
        CreateFormat.formatJrame(this, "System Education Manager Course");
        new ChaoJDialog(this, true).setVisible(true);
        new DangNhapJDialog(this, true).setVisible(true);
        String name = Authentication.user.getHoTen();
        String a = Authentication.user.getVaiTro() == 1 ? "Truong Phong : " + name : "Nhân Viên :  " + name;
        lblUser.setText(a);

    }

    private void openLogin() {
        if (Authentication.user != null) {
            mess.messageNotify(this, "You is Login", "Waring");
        } else {
            new DangNhapJDialog(this, true).setVisible(true);
            changUser();
        }

    }

    private void openChangePassword() {
        if (Authentication.isLogin()) {
            new DoiMatKhauJDialog(this, true).setVisible(true);
        } else {
            mess.messageNotify(this, "Please Login", "Notify");
        }
    }

    private void Logout() {

        new DangNhapJDialog(this, true).setVisible(true);
        changUser();

    }

    private void changUser() {
        String name = Authentication.user.getHoTen();
        String a = Authentication.user.getVaiTro() == 1 ? "Truong Phong : " + name : "Nhân Viên :  " + name;
        lblUser.setText(a);
    }

