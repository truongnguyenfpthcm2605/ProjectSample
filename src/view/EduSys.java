    public void autoImage() {
        // D:\DuAnMauJava\EduSys\Logos
        String[] img = {"JAVA-ONLINE.png",  "lap-trinh-web-la-gi.jpg","lo-trinh-fe_zzhxml.png"};
        new Thread(() -> {
            int i = 0;
            while (true) {
                lblBanner.setIcon(Ximage.read(img[i],lblBanner));
                try {
                    Thread.sleep(2000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(EduSys.class.getName()).log(Level.SEVERE, null, ex);
                }
                i++;
                if (i > img.length - 1) {
                    i = 0;
                }
            }
        }).start();
    }

