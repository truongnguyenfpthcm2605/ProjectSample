    public void edit() {
        String id = (String) tblList.getValueAt(row, 0);
        learner = dao.selectByID(id);
        setForm(learner);
        tabs.setSelectedIndex(1);
        updatestates();
    }

    public void first() {
        this.row = 0;
        this.edit();
    }

    public void pre() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    public void next() {
        if (this.row > tblList.getRowCount() - 1) {
            this.row++;
            this.edit();
        }

    }

    public void last() {
        this.row = tblList.getRowCount() - 1;
        this.edit();
    }

    public void fillTable() {
        tableModel = (DefaultTableModel) tblList.getModel();
        tableModel.setRowCount(0);
        try {
            List<NguoiHoc> list = dao.selectByKeyWord(txtSearch.getText());
            list.stream().forEach((e) -> {
                tableModel.addRow(new Object[]{e.getMaNH(), e.getHoTen(), isGioiTinh(e.isGioiTinh()), e.getNgaySinh(), e.getDienThoai(), e.getEmail(), e.getGhiChu(), e.getMaNV(), e.getNgayDK()});
            });
        } catch (Exception e) {
            mess.messageNotify(this, "Query Error", "Error");
        }
    }

    public String isGioiTinh(boolean a) {
        if (a) {
            return "Nam";
        } else {
            return "Nu";
        }
    }

    public void setForm(NguoiHoc learner) {
        txtMaNH.setText(learner.getMaNH());
        txtHoVaTen.setText(learner.getHoTen());
        isGender(learner.isGioiTinh());
        txtDienThoai.setText(learner.getDienThoai());
        txtEmail.setText(learner.getEmail());
        txtGhichu.setText(learner.getGhiChu());
        try {
            txtNgaySinh.setText(XDate.toString(learner.getNgaySinh(), "yyyy-mm-dd"));    
        } catch (Exception e) {
            txtNgaySinh.setText("");       
        }
    }

    public NguoiHoc getForm() {
        Date now = new Date(); 
        learner = new NguoiHoc();
        learner.setMaNH(txtMaNH.getText());
        learner.setHoTen(txtHoVaTen.getText());
        learner.setGioiTinh(rdoNam.isSelected());
        learner.setNgaySinh(XDate.toDate(txtNgaySinh.getText(), "yyyy-mm-dd"));
        learner.setDienThoai(txtDienThoai.getText());
        learner.setEmail(txtEmail.getText());
        learner.setGhiChu(txtGhichu.getText());
        learner.setMaNV(Authentication.user.getMaNV());
        learner.setNgayDK(now);
        return learner;

    }

    public void updatestates() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblList.getRowCount() - 1);
        // States form
        if (Authentication.user.getVaiTro() == 1) {
            btnAdd.setEnabled(true);
            btnEdit.setEnabled(true);
            btnDelete.setEnabled(true);
        } else {
            btnDelete.setEnabled(false);
        }
        // States
        btnFirst.setEnabled(edit && !first);
        btnReverse.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    public void clearNV() {
       learner = new NguoiHoc();
        setForm(learner);
        row = -1;
        this.updatestates();
    }

    public void isGender(boolean a) {
        if (a) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

    public void search() {
        this.fillTable();
        this.clearForm();
        this.row = -1;
        this.updatestates();
    }

