    public void insert() {
        learner = getForm();
        if (dao.selectByID(learner.getMaNH()) != null) {
            mess.messageNotify(this, "You can't the same Id", "Error");
        } else {
            try {
                if (mess.confirmMessage(this, "Do you want to insert this learner ?", "Question") == JOptionPane.YES_OPTION) {
                    dao.insert(learner);
                    fillTable();
                    this.clearForm();
                    mess.messageNotify(this, "Insert successfull", "Success");
                }

            } catch (Exception e) {
                mess.messageNotify(this, "Insert  fail", "Error");
            }
        }

    }

    public void update() {
        learner = getForm();
        try {
            if (mess.confirmMessage(this, "Do you want to update this learner ?", "Question") == JOptionPane.YES_OPTION) {
                dao.update(learner);
                fillTable();
                this.clearForm();
            }
            mess.messageNotify(this, "Update successfull", "Success");
        } catch (Exception e) {
            mess.messageNotify(this, "Update fail", "Error");
        }
    }

    public void delete() {
        if (!data.checkIsEmpty(txtMaNH)) {
            mess.messageNotify(this, "Please enter MANH you want to delete!", "Notify");
        } else {
            try {
                if (mess.confirmMessage(this, "Do you want to delete this learner?", "Warring") == JOptionPane.YES_OPTION) {
                    dao.delete(txtMaNH.getText());
                    this.fillTable();
                    clearForm();
                    mess.messageNotify(this, "Delete successful", "Success");
                }
            } catch (Exception e) {
                mess.messageNotify(this, "Delete Fail, MANH is not found, ", "Error");
            }
        }
    }

