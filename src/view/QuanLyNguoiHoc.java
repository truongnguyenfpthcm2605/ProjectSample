    private boolean checkForm() {
        if (!data.checkIsEmpty(txtMaNH)) {
            data.changColorLabel(false, lblerrMaNguoiHoc, "MANH is not empty");
            return false;
        } else {
            data.changColorLabel(true, lblerrMaNguoiHoc, null);
        }
        if (!data.checkIsEmpty(txtHoVaTen)) {
            data.changColorLabel(false, lblerrHovaTen, "Full Name is not empty");
            return false;
        } else {
            data.changColorLabel(true, lblerrHovaTen, null);
        }
        if (!data.checkIsEmpty(txtNgaySinh)) {
            data.changColorLabel(false, lblserrNgaySinh, "Date of birth is not empty");
            return false;
        } else if (!data.checkDate(txtNgaySinh.getText())) {
            data.changColorLabel(false, lblserrNgaySinh, "Date Format (yyy-mm-dd)");
        } else {
            data.changColorLabel(true, lblserrNgaySinh, null);
        }
        if (!data.checkIsEmpty(txtDienThoai)) {
            data.changColorLabel(false, lblerrdienthoai, "Phone Number is not empty");
            return false;
        } else if (!data.checkNumberPhone(txtDienThoai.getText())) {
            data.changColorLabel(false, lblerrdienthoai, "Number  is number");
        } else {
            data.changColorLabel(true, lblerrdienthoai, null);
        }
        if (!data.checkIsEmpty(txtEmail)) {
            data.changColorLabel(false, lblerrEmail, "Email is not empty");
            return false;
        } else if (!data.checkEmail(txtEmail.getText())) {
            data.changColorLabel(false, lblerrEmail, "Email wrong format");
            return false;
        } else {
            data.changColorLabel(true, lblerrEmail, null);
        }
        if (txtGhichu.getText().isEmpty()) {
            data.changColorLabel(false, lblerrGhiChu, "Describe is not empty");
            txtGhichu.setBackground(Color.yellow);
            return false;
        } else {
            txtGhichu.setBackground(Color.WHITE);
            data.changColorLabel(true, lblerrGhiChu, null);
        }
     
        return true;
    }

